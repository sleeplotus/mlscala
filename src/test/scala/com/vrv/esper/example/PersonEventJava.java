package com.vrv.esper.example;

public class PersonEventJava {
    private String name;
    private int age;

    public PersonEventJava(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
